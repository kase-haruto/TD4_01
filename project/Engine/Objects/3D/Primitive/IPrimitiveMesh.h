struct MeshResource;
/*-----------------------------------------------------------------------------------------
 * IPrimitiveMesh
 * - プリミティブメッシュインターフェース
 * - 単純形状オブジェクトの共通インターフェースを定義
 *---------------------------------------------------------------------------------------*/
class IPrimitiveMesh{
public:
	//===================================================================*/
	//			public methods
	//===================================================================*/
	virtual ~IPrimitiveMesh() = default;

	virtual void Update(float dt) = 0;

	// 現在のメッシュへの参照を返す
	virtual MeshResource& Mesh() = 0;

	// 内部パラメータ変更時の再生成
	virtual void Rebuild() = 0;
};