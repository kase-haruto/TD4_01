/////////////////////////////////////////////////////////////////////////////////////////
//		オブジェクトタイプからストリグに
/////////////////////////////////////////////////////////////////////////////////////////
static const char* ObjectTypeToString(ObjectType type) {
	switch(type) {
	case ObjectType::Camera:
		return "Camera";
	case ObjectType::Light:
		return "Light";
	case ObjectType::GameObject:
		return "GameObject";
	case ObjectType::Effect:
		return "ParticleSystem";
	case ObjectType::Event:
		return "Event";
	default:
		return "None";
	}
}

